// Operators & Expressions

// Basic Operator ;

// Arithmetic Operator ( + , - , * , / , % , ** )

console.log(10+2); // Output : 12
console.log(10-2); // Output : 8
console.log(10*2); // Output : 20
console.log(10/2); // Output : 5
console.log(10%2); // Output : 0
console.log(10**2); // Output : 100

// Comparison Operator ( == , === , != , !==, < , > , <= , >= )


console.log( 10 == "10" ); // Output : true
console.log( 10 === "10" ); // Output : false
console.log( 10 != "10" ); // Output : false
console.log( 10 !== "10" ); // Output : true
console.log( 10 < 20 ); // Output : true
console.log( 10 > 5 ); // Output : true
console.log( 10 <= 20); // Output : true
console.log( 10 >= 30); // Output : false

// logical operator ( && , || , ! )

// && ( and ) = Result is true when both conditions are true

console.log( true && true);  // Output : true
console.log( true && false); // Output : false
console.log( false && true); // Output : false
console.log( false && false);  // Output : false

// || ( or )  = Result is true when any one condition is true

console.log( true || true);  // Output : true
console.log( true || false); // Output : true
console.log( false || true); // Output : true
console.log( false || false);  // Output : false

// ! ( not ) = to give opposite value

console.log(!(true)); // Output : false